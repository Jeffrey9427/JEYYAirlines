package FileHandler;

public class ConsoleColor {
    // Reset
    public final String RESET = "\033[0m"; // Text Reset

    // Bold
    public final String BLACK_BOLD = "\033[1;30m"; // BLACK
    public final String RED_BOLD = "\033[1;31m"; // RED

    // Background
    public final String CYAN_BACKGROUND = "\033[46m"; // MAGENTA
    public final String GREY_BACKGROUND = "\033[48;5;247m"; // GRAY

    // Bright backgrounds
    public final String CYAN_BACKGROUND_BRIGHT = "\033[0;106m";// MAGENTA
}
